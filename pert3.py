# kita akan belajar tipe data (jenis2 data)

x = 10  # bilangan bulat (integer "int")
print("nilai dari x :", x)
print(type(x))

y = 3.5  # bilangan pecahan (float "float")
print("nilai dari y :", y)
print(type(y))

nama = "baiq bunga"  # tipe data string (tulisan "str")
print("nilai dari nama :", nama)
print(type(nama))

kebenaran = False  # tipe data benar salah (True / False "bool")
print("nilai dari kebenaran :", kebenaran)
print(type(kebenaran))
